console.log("For loop:fruits, fruit salad, fruit salad recipe");

for(let i=1;i<=5;i++)
{
        if (i%2==0) 
         {
              console.log("Fruits:"+i);
         }

        if (i%3==0)       
         {
              console.log("Fruit Salad:"+i);   
         }
         
         if (i%5==0)
         {
             console.log("Fruit Salad Recipe:"+i);
         }
}